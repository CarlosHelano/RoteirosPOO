public class ExercicioSala07 {
    
    public static void main(String[] args) {
        
        System.out.println("Exibindo os numeros de 1 a 100");
        System.out.println("Repeticao com for");
        
        for (int i = 1; i <= 100; i++){
            System.out.println("i : " + i);
        }
       
        System.out.println("Exibindo os numeros de 1 a 100");
        System.out.println("Repeticao com while");
        
        int cont = 1;
        while (cont <= 100)
        {
           System.out.println("cont : " + cont);
           cont++;
        }
        
    }
    
}
